var currentRow = 15;
var currentCol = 15;

function displayMatrix() {
	var content = "";

	content = "<table border='1' width='100%'>";

	for (var i = 0; i < 30; i++) {
		content += "<tr>";
		for (var j=0; j < 30; j++) {
			if (i == currentRow && j == currentCol) {
				content += "<td style='background-color: green' width='32'>";
				content += "<img src='salvi.jpg' width='32'>";
			} else {
				content += "<td width='32'>";
				content += "<img src='cheeseburger.jpg' width='32'>"
			}
			content += "</td>";
		}

		content += "</tr>";
	}

	content += "</table>";
    return content;
}

function draw() {
	var myContent = displayMatrix();
	document.body.innerHTML = "";
	document.write(myContent);
}

function move() {
	//randomly choose if to move up down left or right
    matrix[currentRow][currentCol] = 0;
	var randomDirection = Math.floor((Math.random() * 4) + 1);
	if (randomDirection == 1) {   //up
           currentRow--;
	} else if (randomDirection == 2) {   //down
			currentRow++;
	} else if (randomDirection == 3) { //left
			currentCol--;
	} else {  //right
			currentCol++;
	}
	matrix[currentRow][currentCol] = 1;
	draw();
} 

setInterval(move, 250);



draw();